'''
Ejercicio 6:
Programa que calcule  la media de 3 numeros


Entradas:
numero 1:int->num1
numero 2:int->num2
numero 3:int->num3

 
Salida:
 media=int
'''



num1 = int(input("Introduce el número 1:"))
num2 = int(input("Introduce el número 2:"))
num3 = int(input("Introduce el número 3:"))
media = (num1 + num2 +num3) / 3
print("La media es:", media)